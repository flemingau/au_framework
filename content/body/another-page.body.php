<h1>Another Page</h1>
<p>This page shows you how to create a page name with multiple words and how the title defaults to the name of the page.</p>
<ol>
	<li>Create a new file in the content/body folder (e.g. test-page.body.php)</li>
	<li>Update the .htaccess file to create new routes with the query key (e.g. p=test-page)</li>
	<li>Finally just start editing the .body.php file you created with the content you want displayed</li>
</ol>